/**
  * @defgroup GSM_AT_PARSER Robust parsing of modem responses
  * @brief Token-based UART response reading for AT commands.
  * @{
  */

/**
  * @enum GSM_WaitResult
  * @brief Possible outcomes while waiting for a modem response token.
  */
typedef enum
{
    GSM_RES_OK = 0,      /**< The "OK" token was detected. */
    GSM_RES_ERROR,       /**< The "ERROR" token was detected. */
    GSM_RES_PROMPT,      /**< The ">" prompt was detected. */
    GSM_RES_TIMEOUT      /**< Timeout expired without expected token. */
} GSM_WaitResult;

/**
  * @brief Read modem output until a token (OK/ERROR/PROMPT) is detected or timeout expires.
  * @details
  * The function reads bytes from GSM UART in small slices (10 ms).
  * It accumulates characters into out_buf if provided.
  * After each received byte, it scans for the provided tokens:
  * - token_prm (e.g., ">") first,
  * - token_err (e.g., "ERROR") second,
  * - token_ok  (e.g., "OK") last.
  *
  * If no token is seen within timeout_ms, returns GSM_RES_TIMEOUT.
  *
  * @param out_buf Optional output buffer for received modem text (can be NULL).
  * @param out_sz Size of out_buf; if buffer exists, it is kept null-terminated.
  * @param token_ok Token string indicating success (can be NULL).
  * @param token_err Token string indicating error (can be NULL).
  * @param token_prm Token string indicating prompt (can be NULL).
  * @param timeout_ms Total timeout window in milliseconds.
  * @return GSM_WaitResult indicating which token ended the wait.
  */
static GSM_WaitResult GSM_ReadUntil(uint8_t *out_buf,
                                   uint16_t out_sz,
                                   const char *token_ok,
                                   const char *token_err,
                                   const char *token_prm,
                                   uint16_t timeout_ms)
{
    uint8_t ch;                         /**< One received byte from UART. */
    uint16_t idx = 0;                   /**< Write index into out_buf. */
    uint32_t start = HAL_GetTick();     /**< Start time for total timeout measurement. */

    if (out_buf != NULL && out_sz > 0)
        out_buf[0] = 0;                 /**< Initialize buffer as empty C-string. */

    while ((HAL_GetTick() - start) < timeout_ms)
    {
        HAL_StatusTypeDef st = HAL_UART_Receive(&GSM_UART_HANDLER, &ch, 1, 10);

        if (st == HAL_OK)
        {
            if (out_buf != NULL && out_sz > 0)
            {
                if (idx < (uint16_t)(out_sz - 1))
                {
                    out_buf[idx++] = ch; /**< Store byte and advance index. */
                    out_buf[idx] = 0;    /**< Keep null-terminated. */
                }
                else
                {
                    out_buf[out_sz - 1] = 0; /**< Prevent overflow; keep string terminated. */
                }
            }

            if (out_buf != NULL)
            {
                if (token_prm && strstr((char*)out_buf, token_prm)) return GSM_RES_PROMPT;
                if (token_err && strstr((char*)out_buf, token_err)) return GSM_RES_ERROR;
                if (token_ok  && strstr((char*)out_buf, token_ok))  return GSM_RES_OK ;
            }
        }
    }

    return GSM_RES_TIMEOUT;
}

/**
  * @brief Send an AT command and wait for "OK" or "ERROR" (robust).
  * @details
  * 1) Transmit the command string over GSM UART.
  * 2) Wait until "OK" or "ERROR" appears in modem output, or timeout occurs.
  *
  * @param cmd AT command string (must include \r\n).
  * @param response Optional response buffer to copy received text into (can be NULL).
  * @param timeout Total timeout in milliseconds.
  * @return 1 if "OK" received, 0 otherwise.
  */
uint8_t GSM_SendCommand(uint8_t *cmd, uint8_t *response, uint16_t timeout)
{
    uint8_t recv_buf[200]; /**< Temporary internal buffer for modem response. */
    memset(recv_buf, 0, sizeof(recv_buf));

    HAL_UART_Transmit(&GSM_UART_HANDLER, cmd, strlen((char*)cmd), 1000);

    GSM_WaitResult res = GSM_ReadUntil(recv_buf, sizeof(recv_buf), "OK", "ERROR", NULL, timeout);

    if (response != NULL)
        strcpy((char*)response, (char*)recv_buf);

    if (res == GSM_RES_OK)
        return 1;

    if (res == GSM_RES_TIMEOUT && response == NULL)
        Log_Error((uint8_t*)"Timeout waiting for modem response");

    return 0;
}

/**
  * @brief Check GSM network registration state via AT+CREG?.
  * @details
  * Sends "AT+CREG?" and waits for "OK".
  * Returns registered if modem reports:
  * - +CREG: 0,1 (registered, home)
  * - +CREG: 0,5 (registered, roaming)
  *
  * @return 1 if registered, 0 otherwise.
  */
uint8_t GSM_CheckNetwork()
{
    uint8_t cmd[] = "AT+CREG?\r\n"; /**< AT command to query registration. */
    uint8_t response[200];         /**< Buffer for modem response text. */
    memset(response, 0, sizeof(response));

    HAL_UART_Transmit(&GSM_UART_HANDLER, cmd, strlen((char*)cmd), 1000);

    GSM_WaitResult res = GSM_ReadUntil(response, sizeof(response), "OK", "ERROR", NULL, 2000);

    if (res != GSM_RES_OK)
        return 0;

    if (strstr((char*)response, "+CREG: 0,1") != NULL || strstr((char*)response, "+CREG: 0,5") != NULL)
        return 1;

    return 0;
}

/**
  * @brief Main GSM flow: init modem, search network, send SMS to all stored numbers.
  * @details
  * LED policy in this function:
  * - At start: Network LED ON (searching)
  * - When sending each SMS: Sending LED ON
  * - On successful send: Success LED ON for 1 second
  * - On any error: all off, then blink Sending LED 5 times
  */
void GSM_SendSMS_Process()
{
    uint8_t cmd_buf[50]; /**< Buffer for formatted AT+CMGS command string. */
    uint8_t ctrlz = 0x1A;/**< Ctrl+Z ASCII (end of SMS text in SIM800L). */

    Log_Success((uint8_t*)"Process Started");

    /* Start in network/searching state */
    LED_ALL_OFF();
    LED_NET_ON();

    if (!GSM_SendCommand((uint8_t*)"ATE0\r\n", NULL, 1000)) {
        Log_Error((uint8_t*)"Failed to disable Echo (ATE0)");
        goto ERROR_STATE;
    }
    Log_Success((uint8_t*)"Echo Disabled (ATE0)");

    if (!GSM_SendCommand((uint8_t*)"AT+CMGF=1\r\n", NULL, 1000)) {
        Log_Error((uint8_t*)"Failed to set Text Mode (CMGF)");
        goto ERROR_STATE;
    }
    Log_Success((uint8_t*)"Text Mode Set (CMGF)");

    uint8_t network_found = 0; /**< Flag that becomes 1 when network registration is confirmed. */
    Log_Success((uint8_t*)"Searching for Network...");

    for (int i = 0; i < 5; i++)
    {
        if (GSM_CheckNetwork())
        {
            network_found = 1;
            break;
        }
        HAL_Delay(2000); /**< Blocking delay between network checks. */
    }

    if (network_found == 0) {
        Log_Error((uint8_t*)"Network registration timeout (CREG)");
        goto ERROR_STATE;
    }
    Log_Success((uint8_t*)"Network Connected Successfully");

    /* Send to all numbers */
    for (uint8_t n = 0; n < PHONE_COUNT; n++)
    {
        LED_NET_OFF();
        LED_OK_OFF();
        LED_SEND_ON();

        Log_Success((uint8_t*)"Sending SMS...");

        sprintf((char*)cmd_buf, "AT+CMGS=\"%s\"\r\n", PHONE_NUMBERS[n]);
        HAL_UART_Transmit(&GSM_UART_HANDLER, cmd_buf, strlen((char*)cmd_buf), 1000);

        uint8_t prompt_resp[120]; /**< Buffer for prompt stage response (waiting for '>'). */
        memset(prompt_resp, 0, sizeof(prompt_resp));

        GSM_WaitResult prm = GSM_ReadUntil(prompt_resp, sizeof(prompt_resp), NULL, "ERROR", ">", 100);

        if (prm != GSM_RES_PROMPT)
        {
            if (prm == GSM_RES_TIMEOUT) Log_Error((uint8_t*)"Timeout waiting for '>' prompt");
            else Log_Error((uint8_t*)"Did not receive '>' prompt from modem");
            goto ERROR_STATE;
        }

        Log_Success((uint8_t*)"Prompt Received, Sending Data...");

        HAL_UART_Transmit(&GSM_UART_HANDLER, (uint8_t*)MESSAGE_TEXT, strlen((char*)MESSAGE_TEXT), 1000);
        HAL_UART_Transmit(&GSM_UART_HANDLER, &ctrlz, 1, 1000);

        uint8_t final_resp[250]; /**< Buffer for final stage response (waiting for OK/ERROR). */
        memset(final_resp, 0, sizeof(final_resp));

        GSM_WaitResult fin = GSM_ReadUntil(final_resp, sizeof(final_resp), "OK", "ERROR", NULL, 5000);

        if (fin == GSM_RES_OK)
        {
            Log_Success((uint8_t*)"SMS Sent Successfully!");
            LED_SEND_OFF();
            LED_OK_ON();
            Delay_ms_Asm(1000);
            LED_OK_OFF();
        }
        else
        {
            if (fin == GSM_RES_TIMEOUT) Log_Error((uint8_t*)"SMS Send Failed (Timeout waiting OK)");
            else Log_Error((uint8_t*)"SMS Send Failed (ERROR received)");
            goto ERROR_STATE;
        }

        Delay_ms_Asm(1000);
    }

    /* back to idle */
    LED_ALL_OFF();
    return;

ERROR_STATE:
    LED_ALL_OFF();
    for (int i = 0; i < 5; i++) {
        GPIOC_TogglePins_ODR_Asm(LED_SENDING_MASK);  /**< Blink sending LED to indicate error. */
        Delay_ms_Asm(100);
    }
}
