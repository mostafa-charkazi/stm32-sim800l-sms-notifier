#define LED_NET_OFF()     GPIOC_ResetPins_Asm(LED_NETWORK_MASK) /**< Turn OFF Network LED (PC13). */

#define LED_SEND_ON()     GPIOC_SetPins_Asm(LED_SENDING_MASK)   /**< Turn ON Sending LED (PC14). */
#define LED_SEND_OFF()    GPIOC_ResetPins_Asm(LED_SENDING_MASK) /**< Turn OFF Sending LED (PC14). */

