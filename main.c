/**
  * @defgroup DELAY_ASM Busy-wait delays in ASM
  * @brief Simple blocking delays (not timer-based).
  * @{
  */

/**
  * @brief Busy-wait loop (decrement + branch).
  * @param loops Number of iterations to wait.
  */
static inline void Delay_Loops_Asm(volatile uint32_t loops)
{
    __asm volatile (
        "1: subs %0, %0, #1 \n" /* loops = loops - 1 */
        "   bne  1b         \n" /* if not zero, branch to label 1 */
        : "+r"(loops)
        :
        : "cc"
    );
}

/**
  * @brief Approximate millisecond delay using busy-wait loops.
  * @details
  * Calibration factor (2000) is approximate and depends on CPU clock.
  * @param ms Milliseconds to wait (approx).
  */
static inline void Delay_ms_Asm(uint32_t ms)
{
    while(ms--)
    {
        Delay_Loops_Asm(2000);
    }
}
