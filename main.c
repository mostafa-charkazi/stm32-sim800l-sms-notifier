/* ============================== main() ============================== */
/**
  * @brief Application entry point.
  * @details
  * Initializes HAL, clock, GPIO, USARTs and then polls UART2.
  * When user sends 's' or 'S' on UART2, SMS sending process is triggered.
  * @return int (never returns; infinite loop)
  */
int main(void)
{
    uint8_t ch; /**< Single received character from UART2 (debug trigger). */

    HAL_Init();             /**< Initialize HAL library and SysTick. */
    SystemClock_Config();   /**< Configure system clocks. */

    MX_GPIO_Init();         /**< Initialize GPIO (LED pins etc.). */
    MX_USART1_UART_Init();  /**< Initialize USART1 for GSM modem. */
    MX_USART2_UART_Init();  /**< Initialize USART2 for logging/command. */

    Delay_ms_Asm(50);       /**< Small startup delay. */

    while (1)
    {
        if (HAL_UART_Receive(&huart2, &ch, 1, 1) == HAL_OK)
        {
            if (ch == 's' || ch == 'S')
            {
                GSM_SendSMS_Process();
            }
        }

        Delay_ms_Asm(4);
    }
}

